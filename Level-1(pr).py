a=float(input("Enter  Num:"))
#Enter  Num:10
b=float(input("Enter  Num:"))
#Enter  Num:5
op=input("choose operation:")
#choose operation:%
if op=="+":
    print(a+b)
elif op=="-":
    print(a-b)
elif op=="*":
    print(a*b)
elif op=="/":
    print(a/b)
elif op=="%":
    print(a%b)
else:
    print("Invalid operator!")